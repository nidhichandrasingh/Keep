package javalearning;

public class Operators {
	public static void main (String[] args ) {
		int a = 5;
		int b = 6;
		int sum = a+b;
		System.out.println(sum);
		
		
		int c = 77;
		int d = 55;
		int minus = c-d;
		System.out.println(minus);
		
		int A = 5;
		int B = 4;
		int mul = A*B;
		System.out.println(mul);
		
		double C = 8;
		double D = 4;
		double div = C/d;
		System.out.println(div);
		
		double e = 5;
		double f = 2;
		double modulo = e%f;
		System.out.println(modulo);
	}

}
